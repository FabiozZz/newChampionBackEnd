export const generateColorOnArray = array => {
	let result = [];
	for (let i = 0; i < array.length; i++) {
		result.push(
			array[i].total > 67
				? '#43BF41'
				: array[i].total <= 67 && array[i].total > 34
				? '#E9E238'
				: '#E50E0F'
		);
	}
	return result;
};
